    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
   int ligneSelectionnee = tableEtudiants.getSelectedRow();
    if (ligneSelectionnee == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Sélectionnez un étudiant à modifier !");
        return;
    }

    int id = Integer.parseInt(tableEtudiants.getValueAt(ligneSelectionnee, 0).toString());
    
    String nom = txtNom.getText();
    String filiere = txtFiliere.getText();
    String niveau = txtNiveau.getText();
    
   entites.Etudiant e = new entites.Etudiant(nom, filiere, niveau);
    e.setId(id); // Important : on garde le même ID !

    if (dao.update(e)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Modification réussie !");
        chargerTableau();
        txtNom.setText("");
        txtFiliere.setText("");
        txtNiveau.setText("");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de la modification.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnModifierActionPerformed

    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed

if (txtNom.getText().trim().isEmpty() || txtFiliere.getText().trim().isEmpty()) {
    
    javax.swing.JOptionPane.showMessageDialog(this, 
        "Veuillez remplir tous les champs (Nom et Filière) !", 
        "Champs manquants", 
        javax.swing.JOptionPane.WARNING_MESSAGE);
    
   return; 
}
  String nom = txtNom.getText();
        String filiere = txtFiliere.getText();
        String niveau = txtNiveau.getText();
   
        entites.Etudiant e = new entites.Etudiant(nom, filiere, niveau);
        
       if(dao.create(e)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Étudiant ajouté avec succès !");
            
          chargerTableau();
            txtNom.setText("");
            txtFiliere.setText("");
            txtNiveau.setText("");
            txtNom.requestFocus();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout.");
        }       // TODO add your handling code here:
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed

    int ligneSelectionnee = tableEtudiants.getSelectedRow();
    
    if (ligneSelectionnee == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Veuillez sélectionner un étudiant !");
        return;
    }
    
    int choix = javax.swing.JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer cet étudiant ?", "Confirmation", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (choix == javax.swing.JOptionPane.YES_OPTION) {
      int id = Integer.parseInt(tableEtudiants.getValueAt(ligneSelectionnee, 0).toString());
        
       if (dao.delete(id)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Suppression réussie !");
            chargerTableau();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de la suppression.");
        }
    }      
    }//GEN-LAST:event_btnSupprimerActionPerformed

