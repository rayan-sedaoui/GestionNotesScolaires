    private void txtRechercheKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRechercheKeyReleased
     try {
        String motCle = txtRecherche.getText();
        
       javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tableEtudiants.getModel();
        model.setRowCount(0);
        
        java.sql.Connection conn = connexion.Connexion.getConnection();
        
        String req = "SELECT * FROM etudiant WHERE nom LIKE ?";
        java.sql.PreparedStatement ps = conn.prepareStatement(req);
        
       ps.setString(1, "%" + motCle + "%");
        
        java.sql.ResultSet rs = ps.executeQuery();
                
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"), 
                rs.getString("nom"), 
                rs.getString("filiere") 
            });
        }
    } catch (Exception e) {
        System.out.println("Erreur de recherche : " + e.getMessage());
    }
    }//GEN-LAST:event_txtRechercheKeyReleased


