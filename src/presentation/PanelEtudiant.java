   private void chargerTableau() {
       model.setColumnIdentifiers(new String[]{"ID", "Nom", "Filière", "Niveau"});
        tableEtudiants.setModel(model);
        
       model.setRowCount(0);
        
       for (entites.Etudiant e : dao.findAll()) {
            model.addRow(new Object[]{ e.getId(), e.getNom(), e.getFiliere(), e.getNiveau() });
        }
    }
