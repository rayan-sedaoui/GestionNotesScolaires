  private void chargerComboBoxes() {
        comboEtudiant.removeAllItems(); // On vide la liste par défaut
        comboModule.removeAllItems();

       daoEtudiant.findAll().stream().forEach((e) -> {
            comboEtudiant.addItem(e);
         });

      daoModule.findAll().stream().forEach((m) -> {
            comboModule.addItem(m);
         });
    }

    private void chargerTableau() {
        model.setColumnIdentifiers(new String[]{"ID", "Étudiant", "Module", "Session", "Note"});
        tableNotes.setModel(model);
        model.setRowCount(0);

        daoNote.findAll().stream().forEach((n) -> {
         
            model.addRow(new Object[]{ n.getId(), n.getEtudiant().getNom(), n.getModule().getNom(), n.getSession(), n.getValeur() });
         });
    }
    private void viderChamps() {
        txtSession.setText("");
        txtValeur.setText("");
        comboEtudiant.setSelectedIndex(0);
        comboModule.setSelectedIndex(0);
    }
