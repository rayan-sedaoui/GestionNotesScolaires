    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
     int ligne = tableNotes.getSelectedRow();
    if (ligne == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Sélectionnez une note dans le tableau !");
        return;
    }

    int id = (int) model.getValueAt(ligne, 0);
    int confirmation = javax.swing.JOptionPane.showConfirmDialog(this, "Supprimer cette note ?", "Confirmation", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirmation == javax.swing.JOptionPane.YES_OPTION) {
        if (daoNote.delete(id)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Note supprimée !");
            chargerTableau();
        }
    }   // TODO add your handling code here:
    }//GEN-LAST:event_btnSupprimerActionPerformed

    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
          
        try {
    double note = Double.parseDouble(txtValeur.getText());
    if (note < 0 || note > 20) {
        JOptionPane.showMessageDialog(this, "La note doit être comprise entre 0 et 20 !");
        return;
    }
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Veuillez saisir un nombre valide pour la note !");
    return;
}
        
        if (txtValeur.getText().trim().isEmpty() || txtSession.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Erreur : Tous les champs doivent être remplis !", "Champ vide", JOptionPane.WARNING_MESSAGE);
    return; 
}                               
        try {
        
            entites.Etudiant etudiant = (entites.Etudiant) comboEtudiant.getSelectedItem();
            entites.Module module = (entites.Module) comboModule.getSelectedItem();
            
           
            String session = txtSession.getText();
            String valeurTexte = txtValeur.getText();
            
         
            if (session.isEmpty() || valeurTexte.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Veuillez remplir la session et la note !");
                return;
            }
            
           
            double valeur = Double.parseDouble(valeurTexte);
            
            entites.Note n = new entites.Note(etudiant, module, session, valeur);
            
           if(daoNote.create(n)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Note ajoutée avec succès !");
                chargerTableau();
                
         
                txtSession.setText("");
                txtValeur.setText("");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout de la note.");
            }
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Attention : La note doit être un nombre valide (ex: 15.5) !");
        }
            // TODO add your handling code here:
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void tableNotesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNotesMouseClicked
                                           
    int ligne = tableNotes.getSelectedRow();
    
  String nomEtudiant = model.getValueAt(ligne, 1).toString();
    String nomModule = model.getValueAt(ligne, 2).toString();
    
    // On cherche dans les combos l'objet qui correspond au nom
    for (int i = 0; i < comboEtudiant.getItemCount(); i++) {
        if (comboEtudiant.getItemAt(i).toString().equals(nomEtudiant)) {
            comboEtudiant.setSelectedIndex(i);
        }
    }
    
    for (int i = 0; i < comboModule.getItemCount(); i++) {
        if (comboModule.getItemAt(i).toString().equals(nomModule)) {
            comboModule.setSelectedIndex(i);
        }
    }

    txtSession.setText(model.getValueAt(ligne, 3).toString());
    txtValeur.setText(model.getValueAt(ligne, 4).toString());
    // TODO add your handling code here:
    }//GEN-LAST:event_tableNotesMouseClicked

    private void btnModifieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifieActionPerformed
                                          
    int ligne = tableNotes.getSelectedRow();
    if (ligne == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Sélectionnez d'abord une note dans le tableau !");
        return;
    }

    try {
      
        int id = (int) model.getValueAt(ligne, 0);
        
     
        entites.Etudiant etudiant = (entites.Etudiant) comboEtudiant.getSelectedItem();
        entites.Module module = (entites.Module) comboModule.getSelectedItem();
        String session = txtSession.getText();
        double valeur = Double.parseDouble(txtValeur.getText());

      entites.Note n = new entites.Note(id, etudiant, module, session, valeur);
  if (daoNote.update(n)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Note modifiée avec succès !");
            chargerTableau(); // Rafraîchir l'affichage
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erreur : Vérifiez la saisie de la note.");
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnModifieActionPerformed

