    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try {
       String nom = jTextField1.getText();

       int coefficient = Integer.parseInt(jTextField2.getText());

      
        entites.Module nouveauModule = new entites.Module(nom, coefficient);

        dao.ModuleDaoImpl moduleDao = new dao.ModuleDaoImpl();
        
        if (moduleDao.create(nouveauModule)) {
           
            javax.swing.JOptionPane.showMessageDialog(this, "Module ajouté avec succès !");
            
           
            txtNom.setText("");
            txtCoefficient.setText("");
            
           refreshTable();
            
       } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout en base de données.");
        }
        
    } catch (NumberFormatException e) {
      javax.swing.JOptionPane.showMessageDialog(this, "Attention : Le coefficient doit être un nombre !");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                                        
        try {
           int ligneSelectionnee = tableModules.getSelectedRow();
            
            if (ligneSelectionnee == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Veuillez d'abord sélectionner un module dans le tableau !");
                return; 
            }
            
           int idModule = Integer.parseInt(tableModules.getValueAt(ligneSelectionnee, 0).toString());
            
            int confirmation = javax.swing.JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer ce module ?", "Confirmation", javax.swing.JOptionPane.YES_NO_OPTION);
            
            if (confirmation == javax.swing.JOptionPane.YES_OPTION) {
                dao.ModuleDaoImpl dao = new dao.ModuleDaoImpl();
               dao.delete(idModule);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Module supprimé avec succès !");
                
              refreshTable();
            }
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de la suppression : " + e.getMessage(), "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
        }                                        
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
   
        try {
          int ligneSelectionnee = tableModules.getSelectedRow();
            
            if (ligneSelectionnee == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Veuillez sélectionner un module dans le tableau pour le modifier !");
                return;
            }
            
            int idModule = Integer.parseInt(tableModules.getValueAt(ligneSelectionnee, 0).toString());
            
            String nouveauNom = jTextField1.getText();
            int nouveauCoef = Integer.parseInt(jTextField2.getText());
            
            entites.Module moduleModifie = new entites.Module();
            moduleModifie.setId(idModule); 
            moduleModifie.setNom(nouveauNom);
            moduleModifie.setCoefficient(nouveauCoef);
            
            dao.ModuleDaoImpl dao = new dao.ModuleDaoImpl();
            dao.update(moduleModifie);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Module modifié avec succès !");
            
            jTextField1.setText("");
            jTextField2.setText("");
            
           refreshTable();
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Le coefficient doit être un nombre !", "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de la modification : " + e.getMessage(), "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
        }                                        
          // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

