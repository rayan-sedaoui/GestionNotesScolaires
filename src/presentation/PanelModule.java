private void refreshTable() {
    try {
        dao.ModuleDaoImpl dao = new dao.ModuleDaoImpl();
        java.util.List<entites.Module> liste = dao.findAll();

        String[] colonnes = {"ID", "Nom du Module", "Coefficient"};
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(colonnes, 0);

       for (entites.Module m : liste) {
            Object[] ligne = {m.getId(), m.getNom(), m.getCoefficient()};
            model.addRow(ligne);
        }

       tableModules.setModel(model);

    } catch (Exception e) {
        System.out.println("Erreur chargement tableau : " + e.getMessage());
    }
}

